%% 'CALCULATE' button 
% crop out the droplet and steel rod respectively.
% The input image and 2 cropped images will be shown in axes1, axes2 and
% axes 3 respectively.
% Use the 4 constants, 2 images and 2 methods to calculate.
% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)


% input constants
str1=get(handles.edit1,'string');
a1=1.205;
if isempty(str1)
    r1=a1;
else
    r1 = str2num(str1);
end

str2=get(handles.edit7,'string');
a2=1000;
if isempty(str2)
    r2=a2;
else
    r2 = str2num(str2);
end

str3=get(handles.edit3,'string');
a3=9.80665;
if isempty(str3)
    g=a3;
else
    g = str2num(str3);
end

str4=get(handles.edit4,'string');
a4=6.04;
if isempty(str4)
    d=a4;
else
    d = str2num(str4);
end
% crop the droplet and steel rod by users
global I;
global I1;
global I1_edge;
global I2;

[I1 RECT] = imcrop(I);
[I2 RECT] = imcrop(I);
axes(handles.axes2);
    imshow(I1);
    axes(handles.axes3);
    imshow(I2);

% choose the method to process images, and get the surface tension coefficient
strr=get(handles.popupmenu2,'value');% steel rod, 1 is manually, 2 is auto
str=get(handles.popupmenu1,'value'); % droplet, scenario 1, 2, 3, 4
if strr==1
    if str==1
    Calculate1(r1,r2,g,d,handles);
    elseif str==2
    Calculate2(r1,r2,g,d,handles);
    elseif str==3
    Calculate3(r1,r2,g,d,handles);
    elseif str==4
    Calculate4(r1,r2,g,d,handles);
    end
elseif strr==2
    if str==1
    Calculate5(r1,r2,g,d,handles);
    elseif str==2
    Calculate6(r1,r2,g,d,handles);
    elseif str==3
    Calculate7(r1,r2,g,d,handles);
    elseif str==4
    Calculate8(r1,r2,g,d,handles);
    end
end

